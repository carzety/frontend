/** CONDITIONAL */
const hasil = 88;

if (hasil > 88) {
    console.log(`Selamat Anda Juara I dengan perolehan hasil akhir ${hasil}`);
} else if (hasil > 85) {
    console.log(`Selamat Anda Juara II dengan perolehan hasil akhir ${hasil}`);
} else if (hasil > 80) {
    console.log(`Selamat Anda Juara III dengan perolehan hasil akhir ${hasil}`);
} else {
    console.log(`Selamat Anda Juara Harapan dengan perolehan hasil akhir ${hasil}`);
}